// 1. length()
let str1 = "JavaScript";
console.log(str1.length);

// 2. toUpperCase()
let str2 = "hello";
console.log(str2.toUpperCase());

// 3. toLowerCase()
let str3 = "HELLO";
console.log(str3.toLowerCase());

// 4. charAt()
let str4 = "Programming";
console.log(str4.charAt(3));

// 5. substring()
let str5 = "JavaScript";
console.log(str5.substring(0, 4));

// 6. slice()
let str6 = "Frontend";
console.log(str6.slice(0, 5));

// 7. replace()
let str7 = "I like Java";
console.log(str7.replace("Java", "JavaScript"));

// 8. replaceAll()
let str8 = "cat cat dog";
console.log(str8.replaceAll("cat", "cow"));

// 9. trim()
let str9 = "  Hello World  ";
console.log(str9.trim());

// 10. split()
let str10 = "HTML,CSS,JS";
console.log(str10.split(","));

// 11. concat()
let a = "Hello";
let b = "World";
console.log(a.concat(" ", b));

// 12. includes()
let str11 = "JavaScript Language";
console.log(str11.includes("Script"));

// 13. startsWith()
let str12 = "Welcome Home";
console.log(str12.startsWith("Welcome"));

// 14. endsWith()
let str13 = "index.html";
console.log(str13.endsWith(".html"));
